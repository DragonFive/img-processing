function C = m14_cosTrans(N)
% m14_cosTrans Performs 进行余弦变换
% -------------------------------------------------------------------
% 求预先变化对应的矩阵C并返回
% N是对应要处理的图像的大小;
%  Copyright 1992-2015 Ma XiaoLong
%  $Date: 2015/12/18 10:46:18$
% -------------------------------------------------------------------


i=0:N-1;m=0:N-1;
F=cos((pi*(2*i'+1)*m)/(2*N));

b=ones(N);
b(1,:)*(1/N)^(1/2);
a=[b(1,:)*(1/N)^(1/2);b(2:end,:)*(2/N)^(1/2)];
C=F'.*a;