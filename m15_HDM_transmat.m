function H = m15_HDM_transmat(N)
% m15_HDM_transmat Performs 求大小为N的哈达玛变换矩阵
% N为矩阵大小 应该为2的指数
%
% -------------------------------------------------------------------
%  Copyright 1992-2015 Ma XiaoLong
%  $Date: 2015/12/18 17:03:42$
% -------------------------------------------------------------------
if N==1
    H=[1];
    return;
end
H=m15_HDM_transmat(N/2);%递归调用
A=[H';H']';%上边
B=[H';-1*H']';%下边
H=(1/2)^(1/2)*[A;B];